#!/usr/bin/env python3
"""
This module provides a function `concat` to concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    concatenated two strings.

    Args:
        str1 (str): first string
        str2 (str): second string

    Returns:
        str: concatenated string
    """
    return str1 + str2
