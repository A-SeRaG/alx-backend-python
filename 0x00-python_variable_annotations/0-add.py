#!/usr/bin/env python3
"""
This module provides a function `add` to sum two floating-point numbers.
"""
def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns the result.

    Args:
        a (float): first number.
        b (float): second number.

    Returns:
        float: sum of the two numbers.
    """
    return a + b
